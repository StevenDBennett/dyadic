def load_period6_coefficients(
    path: str = "",
) -> list[int]:
    """
    Load period-6 dynatomic coefficients from file.

    The file format has one coefficient per line::

        u^0: <value>
        u^1: <value>
        ...

    Parameters
    ----------
    path : str
        Path to the coefficient file.  If empty, uses the bundled data file.

    Returns
    -------
    list of int
        Coefficients from constant term to leading term (233 entries).
    """
    if not path:
        ref = resources.files(__package__) / "data" / "period6_coefficients_full.txt"
        path = str(ref)
    coeffs: list[int] = []
    with open(path) as f:
        for line in f:
            line = line.strip()
            if not line or not line.startswith("u^"):
                continue
            _, val_str = line.split(":", 1)
            coeffs.append(int(val_str.strip()))
    return coeffs
